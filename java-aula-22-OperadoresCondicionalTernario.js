// OPERA DOR CONDIOCINAL TERNÁRIO
let age = 21;

console.log(
  age >= 18 // SE FOR CONDIÇÃO
    ? 'Você pode Dirigir.' // SE FOR VERDADEIRO
    : 'Você não pode Dirigir.' // SE FOR FALSO
);

console.log(
  age < 18 // SE FOR CONDIÇÃO
    ? 'Voce não pode Dirigir.' // SE FOR FALSO
    : 'Voce pode Dirigir.' // SE FOR VERDADEIRO
);
