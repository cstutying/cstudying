let number = 10;

// ISSO É UM INCREMENTO
// number = number + 1;
number++;

// INCREMENTA APÓS (POR ISSO NÃO MOSTRA NO CONSOLE)
console.log(number++);
console.log(number);

// INCREMENTAR ANTES
console.log(++number);

// DECREMENTO
// console.log('Decremento após:', number--);
// console.log(number--);
console.log('Decremento antes:', --number);

// "INCREMENTAR" MAIS DE UM (COLOCAR)
number += 10;
console.log(number);

// "DECREMENTAR" MAIS DE UM (REMOVENDO)
number -= 2;
console.log(number);

number = number + 20;
console.log(number);

number = number - 20;
console.log(number);
